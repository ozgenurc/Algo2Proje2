    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
        String ayir[], ayir2[];
        int sayac = 0;
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Bilgiler boş bırakılamaz.");
        } else {
            //Ad soyada göre verileri eklemek
            Bilgi yeni = new Bilgi(jTextField1.getText(), jTextField2.getText());
            for (int i = 0; i < dll.getLength(); i++) {
                ayir = dll.getItem(i).split(", ");
                ayir2 = jTextField1.getText().split(" ");
                if (ayir[0].compareToIgnoreCase(jTextField1.getText()) > 0) {

                } else if (ayir[0].compareToIgnoreCase(jTextField1.getText()) < 0) {
                    sayac++;
                } else if (ayir[0].compareToIgnoreCase(jTextField1.getText()) == 0) {

                    String bilgi = dll.getItem(i);
                    String bol[] = bilgi.split("\\[|, |\\]|\\.");
                    Bilgi yeni2 = new Bilgi(bol[0], bol[1]);
                    dll.deleteToIndex(sayac + 1);
                    dll.insertToIndex(yeni2, sayac);
                    for (int j = 2; j < bol.length; j++) {
                        if (!bol[j].equals("")) {
                            yeni2.klupler.add(bol[j]);
                        }
                    }
                    yeni2.klupler.add(jTextArea1.getText());
                    sayac = -1;
                }
            }
            if (sayac == 0) {
                dll.insertToStart(yeni);
                yeni.klupler.add(jTextArea1.getText());
            } else if (sayac == dll.getLength()) {
                dll.insertToIndex(yeni, sayac);
                yeni.klupler.add(jTextArea1.getText());
            } else if (sayac > 0) {
                dll.insertToIndex(yeni, sayac);
                yeni.klupler.add(jTextArea1.getText());
            }
        }
        doldur();
        jTextField1.setText("");
        jTextField2.setText("");
        jTextArea1.setText("");
    }//GEN-LAST:event_ekleActionPerformed

    private void bilgileriGetirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bilgileriGetirActionPerformed
        //adı ve soyadı girilen sporcunun bilgilerini yazdırıyor
        //bilgileri girilen kayıt yok ise mesaj veriyor.
        String kelime;
        String ayir[];
        int say = 0;
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Aranan futbolcunun adı ve soyadı boş bırakılamaz");
            say++;
        } else {
            kelime = jTextField1.getText();
            for (int i = 0; i < dll.getLength(); i++) {
                ayir = dll.getItem(i).split(", ");

                if (ayir[0].equalsIgnoreCase(kelime)) {
                    dlm.clear();
                    dlm.addElement(dll.getItem(i));
                    say++;
                    i = dll.getLength();
                }
            }
            if (say == 0) {
                JOptionPane.showMessageDialog(null, jTextField1.getText() + " Ad Soyadına sahip kayıt bulunamadı");
            }
            jList1.setModel(dlm);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextArea1.setText("");
        }
    }//GEN-LAST:event_bilgileriGetirActionPerformed

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        //adı ve soyadı girilen sporcu listede var iste listeden siliyor
        //bilgileri girilen kayıt yok ise mesaj veriyor.
        String kelime;
        String ayir[];
        int sayac = 0;

        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Kaydı silinecek sporcunun adı ve soyadı boş bırakılamaz.");
        } else {

            for (int i = 0; i < dll.getLength(); i++) {
                ayir = dll.getItem(i).split(", ");

                kelime = jTextField1.getText();

                if (ayir[0].equalsIgnoreCase(kelime)) {

                    dll.deleteToIndex(i + 1);
                    sayac++;
                }
            }
            if (sayac == 0) {
                JOptionPane.showMessageDialog(null, jTextField1.getText() + "Ad Soyadına sahip kayıt bulunamadı.");
            }

        }
        //jList1.setModel(dlm);
        doldur();
        jTextField1.setText("");
        jTextField2.setText("");
        jTextArea1.setText("");
    }//GEN-LAST:event_silActionPerformed

