    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        int satir = 0;
        String str, isim;
        String ayir[];
        try {
            FileReader words = new FileReader("C:\\Users\\ozgen\\Documents\\NetBeansProjects\\AlgProg2Proje2\\src\\algprog2proje2\\sporcu.txt");
            FileReader words2 = new FileReader("C:\\Users\\ozgen\\Documents\\NetBeansProjects\\AlgProg2Proje2\\src\\algprog2proje2\\sporcu.txt");
            try (BufferedReader reader = new BufferedReader(words)) {
                str = reader.readLine();

                while (str != null) {
                    satir++;
                    str = reader.readLine();
                }
            }
            try (BufferedReader reader2 = new BufferedReader(words2)) {
                reader2.readLine();
                for (int j = 0; j < satir - 1; j++) {
                    isim = reader2.readLine();

                    ayir = isim.split(", ");
                    Bilgi yeni = new Bilgi(ayir[0], ayir[1]);
                    dll.insertToEnd(yeni);
                    for (int i = 2; i < ayir.length; i++) {
                        yeni.klupler.add(ayir[i]);
                    }
                }
                doldur();
            }

        } catch (IOException e) {

            JOptionPane.showMessageDialog(null, "Dosya Okunamadı.");
        }
    }//GEN-LAST:event_formWindowOpened

    void doldur() {
        //Jliste linked list'in içindeki verileri ekliyor.
        dlm.clear();
        for (int i = 0; i < dll.getLength(); i++) {
            dlm.addElement(dll.getItem(i));
        }
        jList1.setModel(dlm);
    }

